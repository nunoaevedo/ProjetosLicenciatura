    private void NovaConsultaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_NovaConsultaMouseClicked
        ConsultaJpaController cc = new ConsultaJpaController();
        
        if(cc.consSemFatura()){
            NovaFatura nc = new NovaFatura();
            nc.setVisible(true);
            dispose();
        }
        else JOptionPane.showMessageDialog(null, "Não existem consultas sem fatura.");
        
        
        
    }//GEN-LAST:event_NovaConsultaMouseClicked

    private void ProcurarConsultaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ProcurarConsultaMouseClicked
        FaturaJpaController f = new FaturaJpaController();
        
        
        if(f.getFaturaCount() > 0){           
            ProcurarFatura pc = new ProcurarFatura();
            pc.setVisible(true);
            dispose();
        }else{
            JOptionPane.showMessageDialog(null, "Não existem faturas registadas.");
        }
        
        
        
    }//GEN-LAST:event_ProcurarConsultaMouseClicked

