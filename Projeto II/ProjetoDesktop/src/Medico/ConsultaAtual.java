    private void preencheCampos(){
        
        NomeField.setText(cons.getIdPac().getNome());
        ApelidoField.setText(cons.getIdPac().getApelido());
        DataNascField.setDate(cons.getIdPac().getDataNasc());
        if(cons.getIdPac().getSexo() == 0) SexoField.setText("Masculino");
        else SexoField.setText("Feminino");
        
        
    }
    
    public void setConsulta(Consulta c){
        this.cons = c;
        preencheCampos();
    }

    

