    private void TerminarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TerminarMouseClicked
        
        if(null != Desc.getText())
            cons.setDescricao(Desc.getText());
        
        ConsultaJpaController cCon = new ConsultaJpaController();
        
        Short s = 1;
        cons.setRealizado(s);
        
        try {
            cCon.edit(cons);
            MedMain ca  = new MedMain();
            ca.setVisible(true);
            dispose();
            JOptionPane.showMessageDialog(null, "Consulta terminada.");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Problema a terminar a consulta.");
        }
        
    }//GEN-LAST:event_TerminarMouseClicked

