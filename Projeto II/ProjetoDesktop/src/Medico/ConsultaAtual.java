    private void Terminar1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Terminar1MouseClicked
        
        if(!(Artigo.getSelectedIndex() == 0) ){
            ArtigoJpaController ac = new ArtigoJpaController();
            Artigo a = ac.findArtigo(Artigo.getSelectedItem().toString());
            if(!cons.getArtigoList().contains(a))
                cons.getArtigoList().add(a);
            else
                JOptionPane.showMessageDialog(null, "Artigo já adicionado.");
        }
        else JOptionPane.showMessageDialog(null, "Selecione um artigo.");
        
        
        
    }//GEN-LAST:event_Terminar1MouseClicked

