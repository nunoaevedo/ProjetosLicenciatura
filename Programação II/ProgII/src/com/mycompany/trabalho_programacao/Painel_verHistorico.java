    public void popularTabela(){
        int ordem = 1;
        Object obj[] = new Object[2];
        
        for(String s: utilizador.getHistorico()){
            obj[0] = ordem++;
            obj[1] = s;
            
            tabela.addRow(obj);
        }
        
       
    }
    

