    private void criarButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_criarButtonMouseClicked
        String nome = this.nomeField.getText();
        String nomeCliente = this.nomeClienteField.getText();
        String precoString = this.precoHoraField.getText();
        
        if(nome.equals("")){
            JOptionPane.showMessageDialog(null, "Nome vazio!");
        }else if(nomeCliente.equals("")){
            JOptionPane.showMessageDialog(null, "Nome de Cliente vazio!");
        }else if(precoString.equals("")){
            JOptionPane.showMessageDialog(null, "Pre√ßo vazio!");
        }else{
            
            
            
            try {
                float preco = Float.parseFloat(precoString);
                Utilizador user = Repositorio.getInstance().getUser();
                
                Repositorio.getInstance().addProjeto(nomeCliente, nome, preco, user.getUsername());
                
                JOptionPane.showMessageDialog(null, "Projeto criado!");
                String historico = "Criou o projeto " + nome ;
                Repositorio.getInstance().addHistorico(historico);
                
                Painel_menuUtilizador pInicial = new Painel_menuUtilizador ();  
                JFrame.getFrame().AvancarParaPainel(pInicial, this.jPanel1);
                JFrame.getFrame().setSize(600, 400);
                JFrame.getFrame().setLocationRelativeTo(null);
                
            } catch (ProjetoRepetido ex) {
                JOptionPane.showMessageDialog(null, ex);
            }catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(null, "Insira um preco v√°lido");
            }
            
            
        }
        
        
    }//GEN-LAST:event_criarButtonMouseClicked


