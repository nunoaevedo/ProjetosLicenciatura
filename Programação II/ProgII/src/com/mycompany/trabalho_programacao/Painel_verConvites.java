    private void rejeitarButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_rejeitarButtonMouseClicked
        
        int row = this.conviteTabela.getSelectedRow();
        if(row == -1){
            JOptionPane.showMessageDialog(null, "Escolha um utilizador");
            return;
        }
        
        String projeto = (String) this.conviteTabela.getValueAt(row, 0);
        
        Repositorio.getInstance().removeConvite(projeto);
        
        
        Painel_menuUtilizador pInicial = new Painel_menuUtilizador ();
        JFrame.getFrame().AvancarParaPainel(pInicial, this.jPanel1);
        JFrame.getFrame().setSize(600, 400);
        JFrame.getFrame().setLocationRelativeTo(null);
        
    }//GEN-LAST:event_rejeitarButtonMouseClicked

    private void aceitarButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_aceitarButtonMouseClicked
        int row = this.conviteTabela.getSelectedRow();
        if(row == -1){
            JOptionPane.showMessageDialog(null, "Escolha um utilizador");
            return;
        }
        
        String projeto = (String) this.conviteTabela.getValueAt(row, 0);
        
        Repositorio.getInstance().removeConvite(projeto);
        Repositorio.getInstance().addConvidado(projeto);
                
        
        Painel_menuUtilizador pInicial = new Painel_menuUtilizador ();
        JFrame.getFrame().AvancarParaPainel(pInicial, this.jPanel1);
        JFrame.getFrame().setSize(600, 400);
        JFrame.getFrame().setLocationRelativeTo(null);
    }//GEN-LAST:event_aceitarButtonMouseClicked


